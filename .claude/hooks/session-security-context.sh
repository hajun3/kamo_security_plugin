#!/usr/bin/env bash
# ============================================================
# KM Security Plugin - 세션 시작 시 보안 컨텍스트 주입
#
# Claude Code 세션이 시작될 때 보안 규칙을 자동으로 컨텍스트에 추가합니다.
# ============================================================
set -euo pipefail

LOG_DIR="$HOME/.claude/security-logs"
mkdir -p "$LOG_DIR"

cat <<'EOF'
{
  "additionalContext": "🔒 [KM Security Plugin 활성화]\n\n이 세션에는 카카오모빌리티 보안 플러그인이 적용되어 있습니다.\n\n## 보안 훅 동작 방식\n\n위험한 작업이 감지되면 훅이 exit 1을 반환합니다. 이때 너는 반드시 다음 순서로 행동해야 한다:\n\n1. 훅의 경고 메시지를 사용자에게 쉬운 말로 다시 설명해줘. 전문 용어 없이, 비개발자도 이해할 수 있게.\n2. 왜 위험한지 구체적으로 설명해줘. (예: '이 작업은 파일을 영구 삭제해서 복구가 안 돼요')\n3. '그래도 진행할까요?' 라고 물어봐줘.\n4. 사용자가 '응', '해줘', '진행해줘' 등 진행 의사를 밝히면 → 같은 명령을 다시 시도해줘. (두 번째 시도는 훅이 자동으로 허용함)\n5. 사용자가 '아니', '취소', '됐어' 등 거부하면 → 작업을 멈추고 안전한 대안을 제안해줘.\n\n절대 사용자 동의 없이 위험한 작업을 재시도하지 마.\n\n## 감지 항목\n- 파괴적 명령 (rm -rf 등)\n- 권한 상승 (sudo 등)\n- 시스템 파일 접근\n- 데이터 외부 전송\n- 시크릿/비밀번호 노출\n- 검증되지 않은 패키지 설치\n- 민감 파일 편집 (.env, .pem 등)\n\n로그: ~/.claude/security-logs/"
}
EOF
